package com.pall.portal.common.support.excel;

public class ExcelDataNode{
	/*
	 * 列号
	 */
    private int colNum;
    /*
     * 单元格值
     */
    private String data;
	public int getColNum() {
		return colNum;
	}
	public void setColNum(int colNum) {
		this.colNum = colNum;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
   
 }  
